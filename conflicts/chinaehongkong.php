<h2>Formação</h2>
  <p>Entre 1839 e 1942, ocorreu o conflito entre a China e o Reino Unido na 1° Guerra do Ópio, onde o Reino Unido venceu e com isso pode ficar com a ilha de Hong Kong. 20 anos depois, na 2° Guerra do Ópio, os chineses foram derrotados novamente, dessa vez perdendo a península de Kowloon.
</p>
<img src="../imgs/2chinahong.jpg" alt="2chinahong">
<!--  Fonte não apresentada-->
<!-- <p>Fonte:<a href="">/</a> </p> -->
  <p>Quatro décadas depois, os ingleses aumentaram ainda mais sua influência na região da China, se espalhando pelos chamados “novos territórios", num acordo que teria valor por 99 anos. Quando a data de devolução dos territórios se aproximou, na década de 80, a China ameaçou começar uma invasão para recuperar todo o seu território, incluindo a área de Hong Kong. E então, no dia 1 de Julho de 1997, o Reino Unido transferiu a soberania dos territórios de volta para a China.
</p>
<img src="../imgs/3chinahong.jpg" alt="3chinahong">
<!--  Fonte não apresentada-->
<!-- <p>Fonte:<a href="">/</a> </p> -->
  <p>Acontece que, durante todo o tempo que Hong Kong passou sob controle do Reino Unido, ela cresceu e desenvolveu muito no sistema capitalista, se tornando um dos maiores financeiros do mundo, então quando a China, que é Socialista, voltou ao controle da ilha, ela aceitou o acordo “1 país, 2 sistemas’, permitindo que Hong Kong continuasse seu estilo de vida capitalista por mais 50 anos (até 2047).</p>

<h2>Atualmente</h2>
  <p>O conflito entre a China e Hong Kong acontece pois, mesmo que o acordo ainda não tenha acabado, e analisando bem, vemos que a China está tentando antecipar o fim dele e o controle total sobre Hong Kong, pois para ela, manter o controle sobre a ilha é estratégico, tanto comercialmente quanto geopoliticamente.</p> 
<img src="../imgs/4chinahong.jpg" alt="4chinahong">
