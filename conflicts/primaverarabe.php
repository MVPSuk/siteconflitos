  <h2>O que foi?</h2>
        
  <p>A partir do final de 2010, países do Oriente Médio e do norte da África passaram por uma série de rebeliões cujas consequências são observadas até hoje. Populações de países como Tunísia, Líbia, Egito, entre outros, saíram às ruas para <strong>PROTESTAR CONTRA GOVERNOS AUTOCRÁTICOS E AS PÉSSIMAS CONDIÇÕES SOCIOECONÔMICAS QUE ENFRENTAVAM: </strong> pobreza, falta de serviços públicos e desemprego, sobretudo entre os mais jovens. Essa série de eventos ficou conhecida pelo nome de Primavera Árabe. </p>
  <p>O termo “Primavera” sugeria o florescer das democracias populares, em alusão à Primavera dos Povos, no final do século XVIII, e à Primavera de Praga, em 1968. O diferencial foi que as mobilizações organizadas dessa vez se apoiaram fortemente no uso de redes sociais e, assim, conseguiram superar a censura dos governos repressivos e não só expandir as manifestações, mas torná-las visíveis ao restante do mundo.</p>
      
  <p>Como resultado, essas revoltas populares derrubaram ditaduras que duravam décadas na região, como a da Tunísia. </p>
      
   <h2>Causas</h2> 
  
  <p>De modo geral, as <strong>EXIGÊNCIAS POPULARES SÃO VOLTADAS PARA A MELHORA DA QUALIDADE DE VIDA E PELA LIBERDADE DE EXPRESSÃO</strong>. As revoltas foram motivadas principalmente pela CORRUPÇÃO dos governos autoritários da região, pelos altos índices de desemprego e pela tirania.</p>
  
  <p>A maioria dos governos nos países que passaram pela Primavera Árabe eram regimes opressores, em que o poder e a riqueza ficam condensados nas mãos de uma única família. Diante da crise financeira e da repressão, a população necessitava apenas de um estopim para as manifestações explodirem. </p>

   <h2>Estopim</h2> 
      
  <p>Tudo começou com Mohamed Bouazizi, um ambulante tunisiano de 26 anos na pequena cidade de Sidi Bouzid, onde era constantemente intimidado por policiais por causa da ilegalidade de seu trabalho, problemas com seus produtos e pedidos de suborno.</p>
       
  <p>Na data de 17 de dezembro de 2010, em novo caso de intimidação, policiais confiscaram seu carrinho de frutas por ele não ter licença para vender no local onde alegadamente era necessária.</p>
      
  <p>Bouazizi foi à sede do governo local reclamar e tentar recuperar seus pertences, mas foi ignorado. Sem conseguir mais trabalhar e afetado há anos pelo desemprego, a situação o deixou desesperado. Bouazizi comprou um galão de combustível, jogou o líquido sobre o corpo e, em frente ao prédio do governo, ateou fogo a si mesmo.</p>

