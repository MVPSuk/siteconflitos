<h2>Primavera Árabe e as redes sociais</h2>
  
  <p>A morte do jovem de origem humilde poderia ter sido esquecida em meio ao caos das manifestações que estavam em andamento na Tunísia. Porém, com as mídias sociais o <strong>ACONTECIMENTO GANHOU REPERCUSSÃO E A INDIGNAÇÃO ESPALHOU-SE PELA POPULAÇÃO E PELOS PAÍSES VIZINHOS.</strong> </p>

  <p>Apesar das reivindicações populares serem parecidas em vários países, não há uma liderança unificada. As manifestações são organizadas pelo próprio povo por meio da internet. Esse é um dos motivos da durabilidade da Primavera Árabe e um dos principais motivos pelo qual é tão complexo para os governos tentarem impedir o movimento. 
</p>

