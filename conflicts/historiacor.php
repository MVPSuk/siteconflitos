        <h1>História da Coreia do Norte (antes do século XIX)</h1>
      <h2>Gojoseon</h2>
      <p>A história da península coreana é muito longa, pois começou há 700.000 anos, quando a região passou a ser habitada. Em todo o território, é possível encontrar relíquias do período Neolítico, que teve início há 8.000 anos.
</p>
      <p>Na Era do Bronze, que teve início cerca de 1.500-2.000 a.C., muitas tribos surgiram na Manchúria e no noroeste da Coreia, posteriormente, elas foram unidas por Dungun, - fundador do povo coreano - instituindo Joseon (2333 a.C.).</p>

      
      <h2>3 Reinos e Gaya (Século V)
</h2>
        <p>No século V, as cidades-estados da região foram se unificando gradativamente em ligas tribais, e depois, em reinos. Goguryeo (37 a.C. – 668 d.C.), localizada ao longo do meio do Rio Yalu, foi uma das primeiras ligas tribais a se transformar em reino. Este reino, abrangia uma grande área na Manchúria e na Península e, posteriormente, adotou uma estrutura política semelhante à de um império.</p>
        <p>Além de Goguryeo, havia outros reinos formados: Baekje (18 a.C. -- 660 d.C.), que foi transformado em um estado centralizado durante o reinado de Geunchogo (r. 346 – 375); Silla (57 a.C. – 935 d.C.), com menor desenvolvimento e menor influência chinesa e Gaya (45-562) formado pela união de várias tribos.
</p>
