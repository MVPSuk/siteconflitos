       <h2>Silla unificado e Balhae (Século VIII)
</h2>
        <p>No século VIII, após dominar todas as cidades-estados vizinhas, inclusive Goguryeo, Gaya e Baekje, o Reino de Silla atingiu o auge do seu poder e prosperidade. No mesmo século, os refugiados de Goguryeo estabeleceram o Reino de Balhae, que se localizava no sul da Manchúria. Este novo reino adotou as práticas administrativas e culturais características de Goguryeo, apesar de ser composto por outras populações. Balhae ocupou um território extenso no auge da sua prosperidade (no século IX), estabeleceu relações com Japão e Turquia e sobreviveu até o ano 926.

</p>
