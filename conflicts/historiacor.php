       <h2>Dinastia Joseon (século XV)
</h2>
        <p>Em 1392, no século XV, o General Yi Song-gye derrubou Goryeo e instituiu Joseon, uma nova dinastia. Seus governantes utilizaram um sistema político bastante estável para administrá-la. </p>
      <p>Em 1592, a região foi invadida pelo Japão e, como líder de ataque contra os japoneses, o Almirante Yi Sun-shin se destacou, pois havia inventado o geobukseon (navio tartaruga), o primeiro couraçado de ferro no mundo.</p>
      <p>No reinado do Rei Yeongjo (r. 1724-1776), diante de uma instabilidade política indesejável, uma política de imparcialidade na nomeação dos governantes foi adotada, essa política fortaleceu o reino e trouxe, novamente, a estabilidade política.</p>
