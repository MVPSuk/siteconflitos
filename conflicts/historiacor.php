      <h2>Dinastia Goryeo (século XI)
</h2>
        <p>No século XI, o general Wang Geon fundou a dinastia Goryeo (918-1392), de onde deriva o nome inglês “Korea”. Apesar da Dinastia Goryeo não ter conseguido recuperar as terras perdidas do Reino de Goguryeo, no nordeste da China, tinha uma cultura sofisticada representada pelo céladon. Durante esta dinastia, algumas inovações surgiram, como a primeira prensa móvel.
</p>
