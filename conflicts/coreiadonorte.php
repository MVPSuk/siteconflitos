        <h1>Coreia do Norte</h1>
        <p>A Coreia é um território localizado na Península da Coreia, esta península fica na região nordeste da Ásia e possui fronteiras com a China a noroeste, com a Rússia a nordeste e é separada do Japão a leste pelo Estreito da Coreia e pelo Mar da China Oriental. Atualmente, a Coreia é dividida entre dois países, a Coreia do Norte, oficialmente República Popular Democrática da Coreia (RPDC), e Coreia do Sul, oficialmente República da Coreia (ROCK).
</p>
      
<h2>Dinâmica de Formação da Coreia no Século XX
</h2>


<h3>Ocupação japonesa e movimento de independência
</h3>

<p>No século XIX, a Coreia permaneceu contrária às relações diplomáticas com países ocidentais. Com isso, alguns PAÍSES IMPERIALISTAS PASSARAM A COMPETIR ENTRE SI PELA INFLUÊNCIA SOBRE A PENÍNSULA. Em 1910, o Japão, um dos países imperialistas, anexou a Coreia depois de sair vencedor dos conflitos contra a China e a Rússia.</p>
<p>O processo de colonização despertou o sentimento de PATRIOTISMO NOS COREANOS. Em 1919, manifestantes em todo o país EXIGIRAM A INDEPENDÊNCIA, no entanto, foram reprimidos de forma brutal pelas autoridades japonesas. </p>
<p>Apesar de o movimento ter FRACASSADO, o patriotismo permaneceu nos coreanos, o que levou à criação de um Governo Provisório em Xangai, na China, e a outros conflitos armados contra os japoneses.
</p>
<p>Com a exploração econômica do país, durante o período de colonização, a vida dos coreanos arruinou-se e permaneceu nesta situação até o fim da Segunda Guerra Mundial, em 1945.</p>

<h3>Coreia no final da Segunda Guerra Mundial 
</h3>

<p>Com o fim da Segunda Guerra Mundial, a Coreia foi libertada da ocupação japonesa. Os coreanos, que já haviam lutado muito por isso, se alegraram, porém, a luta para estabelecer um país independente mostrou seu fracasso quando a parte sul da península foi ocupada pelas forças americanas e a parte norte foi controlada pelas tropas soviéticas. Isso GEROU UM PAÍS DIVIDIDO em duas ideologias totalmente diferentes, devido à Guerra Fria. 

</p>
<p>Em 1947 a Assembleia Geral das Nações Unidas decidiu que uma eleição deveria acontecer na Coreia. Entretanto, tal decisão não foi aceita pela URSS, que não concedeu o acesso à parte norte da península. 
</p>
<p>Foi decidido então que as eleições iriam ser realizadas em áreas acessíveis à comissão. Com isso, as eleições iniciais ocorreram em maio de 1948 nas regiões sul do paralelo 38, linha que passou a dividir a Península Coreana em Norte e Sul. Em 1948, Syngman Rhee foi eleito como primeiro presidente da República da Coreia, ele era um intelectual educado nos EUA e havia combatido pela independência anteriormente. Sua visão voltada para a democracia e para a economia de mercado foi crucial na constituição de um governo na Coreia do Sul. Paralelo a isso, Kim Il-sung assumiu a liderança de um regime comunista que se estabeleceu a norte do paralelo 38, com a influência e apoio da União Soviética.
</p>
      
<h3>Guerra das Coreias
</h3>

<p>Devido ao mútuo interesse em domínio total sobre o território, uma tensão muito forte se estabeleceu entre os novos países. Neste contexto, em 1949 a Coreia do Norte havia tentado invadir o sul da península, no entanto, o líder soviético Stalin achava que as forças norte-coreanas não estavam preparadas para o confronto, por isso ele recusou essa investida.

</p>
<p>Um ano depois, o exército norte-coreano já estava em vantagens contra o exército da Coreia do Sul, pois contava com o apoio armamentístico da União Soviética e com apoio militar da China. Dessa forma, com a aprovação de Stalin, em 5 de junho de 1950, a Coreia do Norte, de forma inesperada, atravessou o paralelo 38 e invadiu o território da Coreia do Sul, no intuito de restabelecer uma Coreia unificada.
</p>
<p>Com medo de que o comunismo se alastrasse por todo mundo, o presidente dos EUA, Harry Truman, defendeu que o líder do bloco capitalista deveria interferir no conflito entre as Coreias. Com isso, os EUA enviou tropas americanas para apoiar a Coreia do Sul. Além disso, Truman também buscou ajuda militar da ONU. Na resolução 82 do Conselho de Segurança das Nações Unidas, a organização além de afirmar que o governo da Coreia do Sul era o único governo deste tipo na península, pois foi determinado legitimamente através de eleições, exigiu que as forças norte-coreanas regressassem para o paralelo 38, para que a guerra chegasse ao fim.
</p>
<p>Com isso, em setembro de 1950, a ONU enviou tropas em auxílio à Coreia do Sul, para controlar a invasão norte-coreana. Em outubro do mesmo ano, a China também enviou suas tropas para ajudar a Coreia do Norte, pois não queria uma fronteira de influência estadunidense.
</p>
      
<h3>Consequências </h3>

<p>Os dois países tiveram muitas consequências causadas pelo conflito, como o grande número de mortos, estimado em mais de 5 milhões de pessoas, e uma zona desmilitarizada de 5 km de largura criada durante a guerra.

</p>
<p>Desde o fim da guerra, uma tensão se estabeleceu entre os dois países. A Coreia do Sul, que ainda manteve mais de 20 mil soldados americanos na zona desmilitarizada, tornou- se uma potência tecnológica e econômica. Enquanto a Coreia do Norte, tornou-se um dos países mais fechados do mundo.  
</p>

<h3>Após a Guerra Fria 
</h3>

<p>Em 1980, depois do fim da URSS, um dos seus principais aliados, a Coreia do Norte iniciou o desenvolvimento de armas nucleares, para que a dinastia Kim fosse protegida.
O país assinou o Acordo de Não-Proliferação Nuclear (TNP) em 1985, no mesmo ano em que concluiu a construção de um reator nuclear experimental. O acordo permitia que países como a Coreia do Norte, que não tinham armas nucleares antes de 1967, pudessem utilizar a tecnologia apenas para fins pacíficos. 
Em 2003, o país saiu do TNP.  A partir disso, as organizações internacionais buscaram negociações com a Coreia do Norte, para que armas nucleares não fossem desenvolvidas e a pesquisa nuclear fosse usada apenas para fins pacíficos.
</p>

<h3>Tensões entre Estados Unidos e Coreia do Norte
</h3>

<p>A relação entre EUA e a Coreia do Norte é bastante complexa, envolve muitas tentativas de negociação, divergências e muitos interesses. Além disso, a tensão entre os dois países é constante. Isso acontece porque os EUA possui fortes aliados, como Japão e Coreia do Sul, que fazem fronteira com a Coreia do Norte e que são muitas vezes alvos de testes militares do governo norte coreano. Com isso, o país americano, em busca de dar auxílio aos seus aliados, mantêm bases militares na região, o que faz com que a Coreia do Norte se sinta ameaçada e responda com mais testes militares. 

</p>
<p>Com o governo Trump, as tensões entre os dois países aumentaram, já que a Coreia do Norte tem tido sucesso nos testes de mísseis balísticos de longo alcance e dos avanços na tecnologia de bombas de hidrogênio.
</p>
      
