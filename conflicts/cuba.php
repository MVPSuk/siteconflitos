      <h3>Conclusão</h3>
      <br>
       <p>Com o final da guerra fria e o desmanche do bloco soviético, muitas das políticas econômicas contra Cuba ainda se mantinham, e com a falta do apoio da URSS com a maioria dos recursos, a economia cubana entrou em rápido declínio juntamente com graves problemas de desabastecimento nas diversas áreas, a partir do início do século XXI, a economia se recuperava lentamente, porém eventos como o furacão Michelle, a queda dos preços mundiais do níquel e a queda do investimento estrangeiro atrasaram catastroficamente a retomada da estabilidade do país que juntamente com a pouca extensão territorial atualmente depende em grande parte do turismo da região, sendo mais uma vez afetada pela recente pandemia de COVID-19.
</p>
