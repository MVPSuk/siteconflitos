      <h3>1959
</h3>
      <br>
      <p>Entre as ações tomadas por Fidel destaca-se a reforma agrária, cuja qual desapropriou em grande parte americanos, que acabaram por não receber o valor esperado por suas terras, isso provocou grande descontentamento entre os proprietários americanos e levou o governo dos Estados Unidos a considerar o líder cubano como um inimigo a ser eliminado. Assim, tratou de desestabilizar economicamente o novo governo cubano, impondo, em 1960, o embargo econômico, comercial e financeiro a Cuba e, paralelamente, treinando ex-militares de Batista  para invadir a ilha entre 17 e 21 de abril de 1961, cerca de 1.500 exilados cubanos tentaram uma invasão frustrada na Baía dos Porcos. Foram rechaçados: 300 deles morreram e 1.200 foram aprisionados e cerca de USD $50 milhões foram enviados a Cuba em alimentos e medicamentos, pela libertação dos exilados. As ações americanas levaram o governo cubano a se aproximar da União Soviética e, dois anos mais tarde, tomar rumos governamentais de orientação marxista elaborando assim uma nova constituição chamada de Lei Fundamental.</p>
