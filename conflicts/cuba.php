      <h3>1962</h3>
      <br>
      <p>Após a invasão da Baía dos Porcos, as tensões provocadas pela Guerra Fria e o posicionamento cubano em favor a URSS, Khrushchov decide por instalar secretamente um conjunto de mísseis em Cuba, tais ações atingiram o ápice das tensões entre os dois blocos, após a resolução dos conflitos políticos foi então trocada a retirada dos mísseis em Cuba pela permanência da soberania da ilha, atualmente já se tem a informação de que também haviam mísseis implantados na região da Turquia, por parte dos EUA. 

