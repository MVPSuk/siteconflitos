      <h2>Colonização, república e ditadura</h2>
      <br>
      <br>
      <h3>Século XV - Formação Histórica e Econômica
</h3>
      <br>
      <p>A história escrita da ilha começa com a penetração da Espanha e a criação da Capitania Geral de Cuba, cujo governo é instalado em Havana onde durante aproximadamente 400 anos, a ilha ficou sob o domínio dos espanhóis, que cultivavam tabaco e cana-de-açúcar (ligação com o texto sobre atividades econômicas) com a utilização de mão-de-obra escrava africana até que em 1762 a cidade foi ocupada brevemente pelo Reino da Grã-Bretanha, porém retornou à posse da Espanha depois de uma troca pelo território da Flórida, que atualmente pertence aos Estados Unidos. Uma série de rebeliões ao longo do século XIX não resultaram no domínio espanhol, porém elas reforçaram tensões entre os países que resultaram na Guerra hispano-americana, em 1898.
</p>
       <br>
      <h3>1898 - Independência Cubana
</h3>
      <br>
      <p>Após o fim da guerra hispano-americana, foi assinado o tratado de Paris, que cedia as colônias espanholas de Guam, Filipinas, Porto Rico e Cuba aos Estados Unidos, cuja qual permaneceu no governo da ilha durante 4 anos, e após isso oficializando assim a independência de Cuba.
</p>
       <br>
      <h3>1952
</h3>
      <br>
      <p>Durante os primeiros anos de independência da ilha ainda remanescem resquícios da influência americana sobre a economia e a política de Cuba, tal cenário se manteve até o ano de 1959, quando o ditador Fulgêncio Batista foi destituído pelo movimento de caráter revolucionário, antiamericano e anticapitalista, liderado por Fidel Castro e com o apoio de outros revolucionários como Che Guevara foi criado um movimento revolucionário chamado Movimento 26 de Julho, composto de jovens e estudantes que iniciaram uma luta contra Batista. 
</p>
