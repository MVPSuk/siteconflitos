<p>A barreira, edificada sob a justificativa de proteger Israel dos ataques palestinos, separa as comunidades locais das áreas agricultáveis. Apesar das críticas internacionais, o projeto foi mantido.
</p>
<p>Novos ataques foram iniciados em 2014 de Israel contra a Cisjordânia. Foi a mais violenta ofensiva desde 2005, quando ocorreu cessar-fogo após a promessa da retirada das colônias judaicas dos territórios palestinos.
</p>
<p>Em 53 dias de conflito, no verão de 2014, foram mortos 2,2 mil palestinos. Deles, 1,5 mil eram civis e 538, menores de idade, conforme dados da OCHA (Escritório das Nações Unidas para a Coordenação de Assuntos Humanitários nos Territórios Palestinos Ocupados). Do lado israelense, a contenda resultou em 71 mortes, seis delas de civis.
</p>
