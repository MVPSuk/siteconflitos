        <h2>Origem do Conflito entre Israel e Palestina</h2>
        
      <p>A Palestina está localizada entre o Rio Jordão e o Mar Mediterrâneo, no Oriente Médio e até o início da Primeira Guerra Mundial, em 1914, estava sob o domínio do Império Otomano.
        </p>
        <p>Com a dissolução deste império, a <strong>INGLATERRA PASSOU A ADMINISTRAR A REGIÃO EM 1917</strong>. Calcula-se que até o fim de 1946, a Palestina era habitada por cerca de 1,2 milhão de árabes e 608 mil judeus.
        </p>
        <p>Ao fim do conflito, os judeus iniciaram uma série de movimentos migratórios em uma tentativa de encontrar um novo lar após as perseguições ocorridas na Europa. Assim, a área passou a ser dominada por judeus a partir do fim da Segunda Guerra Mundial.
        </p>
        <p>Para esse povo, a região é denominada "Terra Santa" e "Terra Prometida", mas o conceito de lugar sagrado é partilhado também pelos muçulmanos e cristãos.
        </p>
      
      
        <h2>O que diz a Bíblia?
        </h2>
      
        <p>As razões para estabelecer o estado judeu na região eram baseados em fontes bíblicas.
        </p>
        <p>Os judeus consideram a área entre a África e o Oriente Médio, onde está a Palestina, a terra prometida por Deus ao profeta Abraão.
</p>
        <p>Esta corresponde aos territórios hoje ocupados pelo Estado de Israel, Palestina, Cisjordânia, Jordânia Ocidental, sul da Síria e Sul do Líbano. Os chamados patriarcas bíblicos a receberam após o Êxodo.
</p>
        <p><strong>É ESSA A ALEGAÇÃO DOS JUDEUS SIONISTAS QUE REIVINDICAM A OCUPAÇÃO INTEGRAL DO TERRITÓRIO</strong>. Antes da ocupação no pós-guerra, 4% da população da Palestina era formada por judeus.
</p>
        <p><strong>O DIREITO A PARTIR DA PROMESSA BÍBLICA É REJEITADO PELOS ÁRABES</strong>strong> e dizem que o filho de Abraão, Ismael, é seu antepassado. Desta maneira, a promessa de Deus os incluiria também. Além disso, a reivindicação dos palestinos é baseada no direito à ocupação, ocorrida por 13 séculos.
</p>
<h2>A Ocupação da Palestina
</h2>
        <p>A região foi ocupada 2 mil anos a.C. por povos amoritas, cananeus e fenícios, sendo denominada como Terra de Canaã. A chegada de hebreus de origem semita ocorreu entre 1,8 mil a 1,5 mil a.C.
</p>

<p>Sucessivas invasões marcaram a região. Em 538 a.C., o comandante da Pérsia, Ciro, o Grande, ocupou a região, retomada depois em uma invasão conduzida por Alexandre, o Grande, em 331 a.C. A invasão romana sob a liderança de Pompeu ocorreu em 64 a.C.
</p>
<p>O domínio romano perdurou até 634 d.C. quando a conquista árabe marca o início de 13 séculos de permanência muçulmana na Palestina. Sob o domínio árabe, a Palestina foi alvo de diversas Cruzadas entre 1099 e 1291 e em 1517 começa a ocupação Otomana, que dura até 1917.
</p>
<p>Após investidas da França, sob o comando de Napoleão Bonaparte (1769-1821), a Palestina passa ao domínio do Egito e a revolta árabe se inicia em 1834.</p>
<p>Somente em 1840, o tratado de Londres encerra o domínio egípcio na região e em 1880 começam as manifestações de autonomia árabe.
</p>   
<p>Em 1917, a Palestina é submetida ao mandato britânico. O comando inglês dura até fevereiro de 1947, quando a Inglaterra renuncia ao mandato sobre a Palestina e entrega a maior parte do equipamento bélico aos grupos sionistas.
</p>

      <h2>Causas do Conflito entre Israel e Palestina
</h2>
      
<p>As causas para o conflito são remotas e se tivermos que colocar uma data, certamente seria a expulsão dos judeus pelos romanos no ano 70 d.C., quando os judeus tiveram que se deslocar para o norte da África e a Europa, ato conhecido como Diáspora Judaica.
</p>
<p>No século XIX, porém, na onda dos nacionalismos que surgia na Europa, alguns judeus se congregaram em torno das<strong> IDEIAS SIONISTAS</strong> do húngaro Theodor Herzl (1860-1904). Este defendia que o lar para os judeus deveria ser em "Sião" ou a terra de Israel, a Palestina e, finalmente, os judeus teriam um lar como os outros povos.
</p>
<p>Ao término da Segunda Guerra Mundial (1945), os judeus sionistas passaram a pressionar a realização da criação do Estado Judeu.
</p>
<p>Durante o conflito, 6 milhões de judeus foram exterminados em campos de concentração sob as ordens de Adolf Hitler (1889-1945). Assim, com apoio internacional, principalmente pela ação norte-americana, a região foi dividida em 1948-1949 em três partes: Estado de Israel, Cisjordânia e Faixa de Gaza.
</p>
<p>A divisão, programada pela ONU (Organização das Nações Unidas), previa o repasse de 55% do território aos judeus e 44% permaneceria aos palestinos.
</p>
<p>As <strong>CIDADES DE BELÉM E JERUSALÉM </strong> seriam consideradas território internacional devido ao significado religioso para muçulmanos, judeus e cristãos. No entanto, os representantes árabes não aceitaram as determinações.
</p>

      <h2>Fundação do Estado de Israel</h2>
      
<p>Em 14 de maio de 1948, contudo, foi fundado Israel, após a retirada dos ingleses. No dia seguinte, <strong>EGITO, SÍRIA, JORD NIA E IRAQUE INVADEM ISRAEL</strong> e deflagram a Guerra da Independência, que foi chamada de Nakba ou "catástrofe" pelos árabes.
</p>
<p>A guerra terminou em 1949 e teve como resultado a expulsão de 750 mil palestinos que passaram a viver como refugiados em movimento conhecido como "êxodo de Nakba", dando origem ao Hamas.
</p>
<p>Como resultado da expulsão dos palestinos, Israel aumentou o território em 50%. A extensão de terras foi indicada pela ONU e ocupam 78% da área destinada à Palestina.
</p>

